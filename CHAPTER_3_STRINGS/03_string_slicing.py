#showed that how to slice a string in parts 

a = "abcdefghijklmnopqrstuvwxyz"

print(a[1:3])

print(a[1:9])

print(a[3:14])

b = 'surajsuckatcoding'

print(b[:5])  #prints index from 0 to 5

print(b[2:5]) #prints index from 2 to 5

print(b[2:])  #prints index from 2 to end

print(b[::-1]) #prints reverse string

